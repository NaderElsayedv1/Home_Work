main() {
  List<String> names = ['Ali', 'Mona', 'Ali', 'Omar', 'Mona'];

  for (int i = 0; i < names.length; i++) {
    int count = 0;

    for (int j = 0; j < names.length; j++) {
      if (names[i] == names[j]) {
        count++;
      }
    }
    if (count > 1) {
      print("${names[i]} appears $count times");
    }
  }
}
