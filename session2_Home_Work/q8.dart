/* Write a Dart program that declares two numeric variables and performs addition, subtraction,
 multiplication, division, and modulo on them. Print each result. */

main() {
  num x = 15;
  num b = 7;

  num add = x + b;
  num sub = x - b;
  num mult = x * b;
  num div = x / b;
  num mod = x % b;

  print("addition: $add");
  print("subtraction: $sub");
  print("multiplication: $mult");
  print("division: $div");
  print("modulo: $mod");
}
